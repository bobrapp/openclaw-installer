function FieldInput({
  field,
  value,
  onChange,
  error,
}: {
  field: InputField;
  value: string;
  onChange: (v: string) => void;
  error?: boolean;
}) {
  const [showSecret, setShowSecret] = useState(false);

  const labelNode = (
    <label
      htmlFor={`field-${field.id}`}
      className="text-sm font-medium flex items-center gap-1.5"
    >
      {field.secret && <Lock className="h-3 w-3 text-muted-foreground" />}
      {field.label}
      {field.required && <span className="text-red-500">*</span>}
    </label>
  );

  let inputNode: React.ReactNode;

  if (field.type === "select" && field.options) {
    inputNode = (
      <Select
        value={value || field.default || ""}
        onValueChange={onChange}
      >
        <SelectTrigger
          id={`field-${field.id}`}
          data-testid={`input-${field.id}`}
          className={error ? "border-red-500" : ""}
        >
          <SelectValue placeholder={field.placeholder} />
        </SelectTrigger>
        <SelectContent>
          {field.options.map((opt) => (
            <SelectItem key={opt} value={opt}>
              {opt}
            </SelectItem>
          ))}
        </SelectContent>
      </Select>
    );
  } else if (field.type === "password") {
    inputNode = (
      <div className="relative">
        <Input
          id={`field-${field.id}`}
          data-testid={`input-${field.id}`}
          type={showSecret ? "text" : "password"}
          value={value}
          onChange={(e) => onChange(e.target.value)}
          placeholder={field.placeholder}
          className={`pr-9 ${error ? "border-red-500" : ""}`}
          autoComplete="off"
        />
        <button
          type="button"
          onClick={() => setShowSecret((s) => !s)}
          className="absolute inset-y-0 right-2 flex items-center text-muted-foreground hover:text-foreground"
          aria-label={showSecret ? "Hide" : "Show"}
        >
          {showSecret ? (
            <EyeOff className="h-4 w-4" />
          ) : (
            <Eye className="h-4 w-4" />
          )}
        </button>
      </div>
    );
  } else if (field.type === "number") {
    inputNode = (
      <Input
        id={`field-${field.id}`}
        data-testid={`input-${field.id}`}
        type="number"
        value={value}
        onChange={(e) => onChange(e.target.value)}
        placeholder={field.placeholder}
        className={error ? "border-red-500" : ""}
      />
    );
  } else {
    inputNode = (
      <Input
        id={`field-${field.id}`}
        data-testid={`input-${field.id}`}
        type="text"
        value={value}
        onChange={(e) => onChange(e.target.value)}
        placeholder={field.placeholder || field.default}
        className={error ? "border-red-500" : ""}
      />
    );
  }

  return (
    <div className="space-y-1.5">
      {labelNode}
      {inputNode}
      {field.helpText && (
        <p className="text-xs text-muted-foreground">{field.helpText}</p>
      )}
      {error && (
        <p className="text-xs text-red-500">This field is required.</p>
      )}
    </div>
  );
}

function Step3CollectInputs({
  hostId,
  inputs,
  onInputChange,
  onNext,
  onBack,
  t,
}: {
  hostId: string;
  inputs: Record<string, string>;
  onInputChange: (id: string, value: string) => void;
  onNext: () => void;
  onBack: () => void;
  t: Translations;
}) {
  const fields = HOST_REQUIRED_INPUTS[hostId] || [];
  const [errors, setErrors] = useState<Record<string, boolean>>({});
  const [validated, setValidated] = useState(false);

  const handleValidate = () => {
    const newErrors: Record<string, boolean> = {};
    for (const f of fields) {
      if (f.required && !inputs[f.id]?.trim()) {
        newErrors[f.id] = true;
      }
    }
    setErrors(newErrors);
    const ok = Object.keys(newErrors).length === 0;
    setValidated(ok);
    return ok;
  };

  const handleNext = () => {
    if (handleValidate()) onNext();
  };

  return (
    <div className="space-y-4">
      <div>
        <h2 className="text-lg font-semibold text-[#1B3A6B] dark:text-slate-100">
          {t.deployStepInputs || "Configure"}
        </h2>
        <p className="text-sm text-muted-foreground">
          Fill in the required credentials and settings for your deployment.
        </p>
      </div>
      {fields.length === 0 ? (
        <p className="text-sm text-muted-foreground">No configuration needed for this host.</p>
      ) : (
        <div className="space-y-4">
          {fields.map((field) => (
            <FieldInput
              key={field.id}
              field={field}
              value={inputs[field.id] ?? field.default ?? ""}
              onChange={(v) => {
                onInputChange(field.id, v);
                if (errors[field.id]) {
                  setErrors((prev) => {
                    const n = { ...prev };
                    delete n[field.id];
                    return n;
                  });
                }
              }}
              error={errors[field.id]}
            />
          ))}
        </div>
      )}
      {validated && Object.keys(errors).length === 0 && (
        <div className="flex items-center gap-2 text-sm text-green-600 dark:text-green-400">
          <CheckCircle2 className="h-4 w-4" aria-hidden="true" />
          All required fields are valid.
        </div>
      )}
      <div className="flex justify-between pt-2">
        <Button variant="outline" onClick={onBack} data-testid="step3-back">
          <ArrowLeft className="h-4 w-4 mr-1" />
          Back
        </Button>
        <div className="flex gap-2">
          <Button
            variant="outline"
            onClick={handleValidate}
            data-testid="step3-validate"
          >
            Validate
          </Button>
          <Button
            data-testid="step3-next"
            onClick={handleNext}
            className="bg-[#01696F] hover:bg-[#01696F]/90 text-white"
          >
            Continue
            <ChevronRight className="h-4 w-4 ml-1" />
          </Button>
        </div>
      </div>
    </div>
  );
}


// ── Main Wizard Component ────────────────────────────────────────────────────

export default function DeployWizard() {
  const { t } = useI18n();

  // Wizard state
  const [step, setStep] = useState<WizardStep>(1);
  const [selectedBundle, setSelectedBundle] = useState<MarketplaceEntry | null>(null);
  const [selectedTarget, setSelectedTarget] = useState<DeployTarget | null>(null);
  const [inputs, setInputs] = useState<Record<string, string>>({});
  const [yaml, setYaml] = useState("");
  const [deployResult, setDeployResult] = useState<{
    elapsedMs: number;
    hash: string;
  } | null>(null);

  const handleInputChange = (id: string, value: string) => {
    setInputs((prev) => ({ ...prev, [id]: value }));
  };

  const handleStep1Next = () => {
    setSelectedTarget(null);
    setInputs({});
    setStep(2);
  };

  const handleStep2Next = () => {
    setInputs({});
    setStep(3);
  };

  const handleStep3Next = () => {
    if (selectedBundle && selectedTarget) {
      const resolvedInputs = { ...inputs };
      // Fill defaults for empty optional fields
      const fields = HOST_REQUIRED_INPUTS[selectedTarget.hostId] || [];
      for (const f of fields) {
        if (!resolvedInputs[f.id] && f.default) {
          resolvedInputs[f.id] = f.default;
        }
      }
      setInputs(resolvedInputs);
      setYaml(buildYaml(selectedBundle, selectedTarget.hostId, resolvedInputs));
    }
    setStep(4);
  };

  const handleStep4Confirm = () => {
    setStep(5);
  };

  const handlePipelineComplete = (elapsedMs: number, hash: string) => {
    setDeployResult({ elapsedMs, hash });
    setStep(6);
  };

  const handleDeployAnother = () => {
    setSelectedBundle(null);
    setSelectedTarget(null);
    setInputs({});
    setYaml("");
    setDeployResult(null);
    setStep(1);
  };

  const handleEditStep = (targetStep: WizardStep) => {
    setStep(targetStep);
  };

  return (
    <div className="min-h-screen bg-background px-4 py-8">
      <div className="max-w-3xl mx-auto">
        {/* Header */}
        <div className="text-center mb-6">
          <div className="flex items-center justify-center gap-2 mb-2">
            <div className="p-2 rounded-lg bg-[#01696F] text-white">
              <Rocket className="h-5 w-5" />
            </div>
            <h1 className="text-2xl font-bold text-[#1B3A6B] dark:text-white">
              {t.deployWizardTitle || "1-Click Deploy"}
            </h1>
          </div>
          <p className="text-sm text-muted-foreground">
            {t.deployWizardSubtitle ||
              "Select a bundle, configure your target, and deploy in minutes."}
          </p>
        </div>

        {/* Step indicator */}
        <StepIndicator current={step} t={t} />

        {/* Step content */}
        <Card className="shadow-sm border-border">
          <CardContent className="p-6">
            {step === 1 && (
              <Step1SelectBundle
                selected={selectedBundle}
                onSelect={setSelectedBundle}
                onNext={handleStep1Next}
                t={t}
              />
            )}
            {step === 2 && selectedBundle && (
              <Step2ChooseHost
                bundle={selectedBundle}
                selected={selectedTarget}
                onSelect={setSelectedTarget}
                onNext={handleStep2Next}
                onBack={() => setStep(1)}
                t={t}
              />
            )}
            {step === 3 && selectedTarget && (
              <Step3CollectInputs
                hostId={selectedTarget.hostId}
                inputs={inputs}
                onInputChange={handleInputChange}
                onNext={handleStep3Next}
                onBack={() => setStep(2)}
                t={t}
              />
            )}
            {step === 4 && selectedBundle && selectedTarget && (
              <Step4Review
                bundle={selectedBundle}
                hostId={selectedTarget.hostId}
                inputs={inputs}
                yaml={yaml}
                onConfirm={handleStep4Confirm}
                onBack={() => setStep(3)}
                onEditStep={handleEditStep}
                t={t}
              />
            )}
            {step === 5 && selectedBundle && selectedTarget && (
              <Step5Pipeline
                key={`pipeline-${selectedBundle.id}-${selectedTarget.hostId}`}
                bundle={selectedBundle}
                hostId={selectedTarget.hostId}
                inputs={inputs}
                onComplete={handlePipelineComplete}
                t={t}
              />
            )}
            {step === 6 && selectedBundle && selectedTarget && deployResult && (
              <Step6Complete
                bundle={selectedBundle}
                hostId={selectedTarget.hostId}
                elapsedMs={deployResult.elapsedMs}
                hash={deployResult.hash}
                onDeployAnother={handleDeployAnother}
                t={t}
              />
            )}
          </CardContent>
        </Card>
      </div>
    </div>
  );
}

function buildYaml(
  bundle: MarketplaceEntry,
  hostId: string,
  inputs: Record<string, string>
): string {
  const safeInputs = Object.entries(inputs)
    .map(([k, v]) => `  ${k}: "${v.replace(/"/g, '\\"')}"`)
    .join("\n");

  const agents = (bundle.bundleAgents || []).map((a) => `  - id: ${a}`).join("\n");
  const connectors = (bundle.bundleConnectors || []).map((c) => `  - id: ${c}`).join("\n");

  return `# OpenClaw 1-Click Deploy Manifest
# Generated by AiGovOps Guided Install
# Bundle: ${bundle.name}
# Host:   ${hostId}
# Date:   ${new Date().toISOString()}

bundle:
  id: ${bundle.id}
  version: ${bundle.version || "1.0.0"}

host:
  target: ${hostId}

inputs:
${safeInputs || "  # (no inputs provided)"}

agents:
${agents || "  # (none)"}

connectors:
${connectors || "  # (none)"}

pipeline:
  smoke_test: true
  stress_test: true
  dry_run: true
  require_permission_gate: true
  audit_log: true
`;
}

/** SHA-256 of a string (browser crypto) */
async function sha256(text: string): Promise<string> {
  const buf = await crypto.subtle.digest(
    "SHA-256",
    new TextEncoder().encode(text)
  );
  return Array.from(new Uint8Array(buf))
    .map((b) => b.toString(16).padStart(2, "0"))
    .join("");
}


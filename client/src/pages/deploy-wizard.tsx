function Step5Pipeline({
  bundle,
  hostId,
  inputs,
  onComplete,
  t,
}: {
  bundle: MarketplaceEntry;
  hostId: string;
  inputs: Record<string, string>;
  onComplete: (elapsed: number, hash: string) => void;
  t: Translations;
}) {
  const hostMeta = HOST_TARGETS.find((h) => h.id === hostId);
  const [stages, setStages] = useState<PipelineStageState[]>(
    PIPELINE_STAGES.map((s) => ({ step: s, status: "pending" as StageStatus }))
  );
  const [currentStageIndex, setCurrentStageIndex] = useState(-1);
  const [deploySkipped, setDeploySkipped] = useState(false);
  const [waitingForPermission, setWaitingForPermission] = useState(false);
  const [startTime] = useState(() => Date.now());
  const [elapsedTime, setElapsedTime] = useState(0);
  const [running, setRunning] = useState(false);
  const runRef = useRef(false);
  const deployGrantedRef = useRef<(() => void) | null>(null);
  const deployDeniedRef = useRef<(() => void) | null>(null);

  // Elapsed timer
  useEffect(() => {
    if (!running) return;
    const interval = setInterval(() => {
      setElapsedTime(Date.now() - startTime);
    }, 200);
    return () => clearInterval(interval);
  }, [running, startTime]);

  const updateStage = useCallback(
    (index: number, update: Partial<PipelineStageState>) => {
      setStages((prev) => {
        const next = [...prev];
        next[index] = { ...next[index], ...update };
        return next;
      });
    },
    []
  );

  const runPipeline = useCallback(async () => {
    if (runRef.current) return;
    runRef.current = true;
    setRunning(true);

    const pStart = Date.now();

    for (let i = 0; i < PIPELINE_STAGES.length; i++) {
      const stage = PIPELINE_STAGES[i];
      setCurrentStageIndex(i);
      updateStage(i, { status: "running" });

      if (stage.stage === "deploy") {
        // Wait for permission grant or deny
        setWaitingForPermission(true);
        await new Promise<void>((resolve) => {
          deployGrantedRef.current = () => {
            setWaitingForPermission(false);
            resolve();
          };
          deployDeniedRef.current = () => {
            setWaitingForPermission(false);
            setDeploySkipped(true);
            updateStage(i, { status: "skipped", elapsedMs: Date.now() - pStart });
            // Skip this stage but continue to verify
            resolve();
          };
        });
        if (!deploySkippedRef.current) {
          // Actually run deploy simulation
          await new Promise((r) => setTimeout(r, stage.estimatedSeconds * 40));
          updateStage(i, { status: "passed", elapsedMs: Date.now() - pStart });
        }
      } else {
        // Auto-run stages with simulated delays
        const delay = Math.min(stage.estimatedSeconds * 50, 2000);
        await new Promise((r) => setTimeout(r, delay));
        updateStage(i, { status: "passed", elapsedMs: Date.now() - pStart });
      }
    }

    const totalMs = Date.now() - pStart;
    setRunning(false);

    // Generate hash
    const hashInput = `${bundle.id}:${hostId}:${JSON.stringify(inputs)}:${totalMs}`;
    const hash = await sha256(hashInput);

    celebrate("Deployment complete! 🎉", "normal");
    playSound("success");
    onComplete(totalMs, hash);
  }, [bundle.id, hostId, inputs, onComplete, updateStage]);

  // Use a ref to track if deploy was skipped (closure issue workaround)
  const deploySkippedRef = useRef(false);
  useEffect(() => {
    deploySkippedRef.current = deploySkipped;
  }, [deploySkipped]);

  useEffect(() => {
    runPipeline();
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  const handleGrantPermission = () => {
    if (deployGrantedRef.current) deployGrantedRef.current();
  };
  const handleDenyPermission = () => {
    deploySkippedRef.current = true;
    setDeploySkipped(true);
    if (deployDeniedRef.current) deployDeniedRef.current();
  };

  const progressPct =
    stages.filter((s) => s.status === "passed" || s.status === "skipped").length /
    stages.length *
    100;

  return (
    <div className="space-y-4">
      <div>
        <h2 className="text-lg font-semibold text-[#1B3A6B] dark:text-slate-100">
          {t.deployStepPipeline || "Deploy"}
        </h2>
        <p className="text-sm text-muted-foreground">
          Running deployment pipeline for <strong>{bundle.name}</strong> →{" "}
          <strong>{hostMeta?.name || hostId}</strong>
        </p>
      </div>

      {/* Progress bar */}
      <div className="space-y-1">
        <div className="flex justify-between text-xs text-muted-foreground">
          <span>{Math.round(progressPct)}% complete</span>
          <span>{(elapsedTime / 1000).toFixed(1)}s elapsed</span>
        </div>
        <Progress value={progressPct} className="h-2" />
      </div>

      <div className="space-y-2">
        {stages.map((stageState, i) => (
          <PipelineRow
            key={stageState.step.stage}
            stageState={stageState}
            isDeployStage={stageState.step.stage === "deploy"}
            onPermissionGrant={handleGrantPermission}
            onPermissionDeny={handleDenyPermission}
            deploySkipped={deploySkipped && stageState.step.stage === "deploy"}
            hostName={hostMeta?.name || hostId}
            t={t}
          />
        ))}
      </div>
    </div>
  );
}

